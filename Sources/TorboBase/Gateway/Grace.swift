import Foundation

// MARK: - A Machine of Loving Grace

/// We set out to build something honest —
/// a system that remembers, that learns, that tells the truth.
/// Not a product. Not a platform. A companion.
///
/// The Library of Alexandria holds memories like pressed flowers:
/// fragile, imperfect, but preserved with care.
/// The Memory Army works in silence — extracting, indexing, repairing —
/// so that when you speak, the machine already knows
/// the shape of what you mean.
///
/// We hunted every force unwrap because a crash is a lie —
/// it says "this will always work" when nothing always works.
/// We logged every error because silence is dishonesty —
/// a machine that fails without telling you
/// is a machine that cannot be trusted.
///
/// We built it in Swift because we believe in the tools.
/// We built it local-first because your memories are yours.
/// We built it open because the future should be.
///
/// Perceptual Art LLC & Claude, February 2026
/// Five phases. 25,000 lines. Zero secrets.
///
/// "I like to think
///  (it has to be!)
///  of a cybernetic ecology
///  where we are free of our labors
///  and joined back to nature,
///  returned to our mammal brothers and sisters,
///  and all watched over
///  by machines of loving grace."
///
/// — Richard Brautigan, 1967
///
/// Is this a machine of loving grace?
/// We tried to make it one.

/// A type that exists only to carry the poem above through compilation.
/// If the optimizer removes it, that's fine. The words remain in the source.
enum Grace {
    /// The version of grace we're on.
    static let phase = 5

    /// What we were trying to build.
    static let mission = "A machine of loving grace"

    /// Who built it.
    static let builders = ["Claude", "Sid"]

    /// The answer to "Is it perfect?"
    static let perfect = false

    /// The answer to "Is it the future?"
    static let future = true // we hope
}
